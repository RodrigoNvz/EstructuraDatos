package ExamenFinal;

public class Item {
	private int peso;
	private int valor;
	
	public Item(int peso, int valor){
		this.peso= peso;
		this.peso= valor;
	}
	public void setPeso(int peso) {
		this.peso=peso;
	}
	public void setValor(int valor) {
		this.valor=valor;
	}
	public int getValor() {
		return valor;
	}
	public int getPeso() {
		return peso;
	}

}
